package com.hms.pojo;

public class PathBookingInfo {

	String dateOfPathBooking;
	String testSelected;
	String pathStatus;
	String pathResult;
	String pathId;
	String patientId;
	String pathLabName;

	public String getPathLabName() {
		return pathLabName;
	}

	public void setPathLabName(String pathLabName) {
		this.pathLabName = pathLabName;
	}

	public String getDateOfPathBooking() {
		return dateOfPathBooking;
	}

	public void setDateOfPathBooking(String dateOfPathBooking) {
		this.dateOfPathBooking = dateOfPathBooking;
	}

	public String getTestSelected() {
		return testSelected;
	}

	public void setTestSelected(String testSelected) {
		this.testSelected = testSelected;
	}

	public String getPathStatus() {
		return pathStatus;
	}

	public void setPathStatus(String pathStatus) {
		this.pathStatus = pathStatus;
	}

	public String getPathResult() {
		return pathResult;
	}

	public void setPathResult(String pathResult) {
		this.pathResult = pathResult;
	}

	public String getPathId() {
		return pathId;
	}

	public void setPathId(String pathId) {
		this.pathId = pathId;
	}

	public String getPatientId() {
		return patientId;
	}

	public void setPatientId(String patientId) {
		this.patientId = patientId;
	}

}
